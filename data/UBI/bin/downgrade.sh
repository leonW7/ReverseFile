#!/bin/sh
#

BACKUP_LOCATION=$1
CONFIG_FILE=$2
CURRENT_CONFIG_LOCATION=$3
CUR_MAJOR=$4
CUR_MINOR=$5
CUR_VER=$6
CUT=/usr/bin/cut
RM=/bin/rm
CP=/bin/cp
TAR=/bin/tar


$RM -rf $BACKUP_LOCATION

cd $CURRENT_CONFIG_LOCATION

$TAR -czvf $CONFIG_FILE.tar.bz2 $CONFIG_FILE

mkdir -p $BACKUP_LOCATION

$CP $CURRENT_CONFIG_LOCATION/$CONFIG_FILE.tar.bz2 $BACKUP_LOCATION/$CONFIG_FILE.$CUR_MAJOR.$CUR_MINOR'B'$CUR_VER.tar.bz2

exit 0; 

